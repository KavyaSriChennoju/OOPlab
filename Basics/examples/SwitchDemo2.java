
public class SwitchDemo2 
{
  public static void main(String[] args) 
  {
	  char ch='d';
	 switch(ch)
	 {
	 case 'a':
		   System.out.println("A for Apple");
		   break;
	 case 'b':
		   System.out.println("B for Bat");
		   break;
	 case 'c':
		   System.out.println("C for Cat");
		   break;
	    default:
			   System.out.println("Wrong Choice");  
	 }
  }
}
